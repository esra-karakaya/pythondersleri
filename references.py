# Özetle Python'da "Value Type" Kavramı
# Immutable türler (int, float, str, tuple) değiştirilemez, yani "value type" gibi davranır.
# Mutable türler (list, dict, set) bellekte değiştirilebilir, yani "reference type" gibi davranır.
# Python'da tüm değişkenler referans kullanarak saklanır, ancak immutable türler değiştirildiğinde yeni bir nesne oluşturulur.